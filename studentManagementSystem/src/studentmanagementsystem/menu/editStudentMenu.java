    private void pictureRadioBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pictureRadioBtnActionPerformed
        editState = "picture";
        final JFileChooser fc = new JFileChooser();
        int returnVal = fc.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            fileName = file.getAbsolutePath();
            fileName = fileName.replaceAll("\\\\\\\\", "\\\\");
            fileName = fileName.replace("C:\\", "C:\\\\");
            try {
                BufferedImage image = ImageIO.read(file);
                BufferedImage resizedImage=resize(image,143,143);
                icon = new ImageIcon(resizedImage);
            } catch (IOException ex) {
                Logger.getLogger(createStudentMenu.class.getName()).log(Level.SEVERE, null, ex);
            }
            editPictureLbl.setIcon(icon);
        }
    }//GEN-LAST:event_pictureRadioBtnActionPerformed

