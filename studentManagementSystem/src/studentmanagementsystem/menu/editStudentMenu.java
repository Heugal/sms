    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        int areYouSure = JOptionPane.showConfirmDialog(null, "Are you sure you want to edit the specified user?");
        if (areYouSure == 0){
            boolean pictureChecksum = false;
            String tempIDString = SanitizeInputs.removeNonDigitFromIDString(studentIDTxtField.getText());
            int studentID = (tempIDString.equals("")) ? 0 : Integer.parseInt(studentIDTxtField.getText());
            String firstName = SanitizeInputs.sanitizeInputString(firstNameTxtField.getText());
            String lastName = SanitizeInputs.sanitizeInputString(lastNameTxtField.getText());
            String editText = SanitizeInputs.sanitizeInputString(editTxtField.getText());
            if ((studentID > 0) || ((!firstName.equals("")) && (!lastName.equals("")))){
                String sql = "update students set ";
                if (!editState.equals("")){
                    switch(editState){
                        case "first name":
                            sql = sql+"first_name = '";
                            break;
                        case "last name":
                            sql = sql+"last_name = '";
                            break;
                        case "dob":
                            sql = sql+"dob = '";
                            break;
                        case "address":
                            sql = sql+"address = '";
                            break;
                        case "phone":
                            sql = sql+"phone = '";
                            break;
                        case "email":
                            sql = sql+"email = '";
                            break;
                        case "picture":
                            pictureChecksum = true;
                            sql = "";
                    }
                    if (pictureChecksum == false){
                        if (studentID > 0){
                            sql = ((!firstName.equals("") && (!lastName.equals("")))) ?
                                    sql + editText + "' where student_id = "+studentID
                                    +" and upper(first_name) = upper('"+firstName+"') and "
                                    +"upper(last_name) = upper('"+lastName+"')" :
                                    sql + editText + "' where student_id = "+studentID;
                        }
                        else{
                            sql = sql + editText + "' where upper(first_name) = upper('"
                                    +firstName+"') and upper(last_name) = upper('"
                                    +lastName+"')";
                        }
                        try{
                            UserManager.executeQuery(sql);
                            Item item = new Item();
                            item.setVisible(true);
                            setVisible(false);
                        }catch (SQLException ex) {
                            Logger.getLogger(deleteStudentMenu.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                    else{
                        if (icon != null){
                            Connection conn = null;
                            ResultSet rs = null;
                            try{
                                conn = ConnectDB.getConnection();
                                boolean photoPresent;
                                if (studentID > 0){
                                    photoPresent = ItemManager.checkIfPhotoPresent(studentID);
                                    // if photo present
                                    if (true == photoPresent){
                                        try (PreparedStatement pstmt = conn.prepareStatement("update student_photo set picture = ? where student_id = ?")) {
                                            FileInputStream fis = new FileInputStream(fileName);
                                            pstmt.setBinaryStream(1, fis, (int)fileName.length()); 
                                            pstmt.setInt(2, studentID);
                                            pstmt.executeUpdate();
                                        }
                                    }
                                    else{
                                        try (PreparedStatement pstmt = conn.prepareStatement("insert into student_photo (student_id, picture) values (?, ?)")){
                                            File file = new File(fileName);
                                            FileInputStream fis = new FileInputStream(file);
                                            pstmt.setInt(1, studentID);
                                            pstmt.setBinaryStream(2, fis, (int)file.length()); 
                                            pstmt.executeQuery();
                                        }
                                    }
                                    conn.commit();
                                    conn.close();
                                }
                                else{
                                    // we have to find the student ID because the studentID
                                    // is the foreign key used to connect the student
                                    // table and student_photo table
                                    sql = "select student_id from students where first_name = '"
                                        +firstName+"' and last_name = '" + lastName + "'";
                                        rs = ItemManager.getSearchItems(sql);
                                        if (rs.next()){
                                            studentID = rs.getInt("student_id");
                                            photoPresent = ItemManager.checkIfPhotoPresent(studentID);
                                            if (true == photoPresent){
                                                try (PreparedStatement pstmt = conn.prepareStatement("update student_photo set picture = ? where student_id = ?")) {
                                                    File file = new File(fileName);
                                                    FileInputStream fis = new FileInputStream(file);
                                                    pstmt.setBinaryStream(1, fis, (int)file.length()); 
                                                    pstmt.setInt(2, studentID);
                                                    pstmt.executeUpdate();
                                                }
                                            }
                                            else{
                                                try (PreparedStatement pstmt = conn.prepareStatement("insert into student_photo (student_id, picture) values (?, ?)")){
                                                    File file = new File(fileName);
                                                    FileInputStream fis = new FileInputStream(file);
                                                    pstmt.setInt(1, studentID);
                                                    pstmt.setBinaryStream(2, fis, (int)file.length()); 
                                                    pstmt.executeQuery();
                                                }
                                            }
                                            conn.commit();
                                            conn.close();
                                        }
                                }                            
                            }catch ( SQLException | FileNotFoundException ex) {
                                    Logger.getLogger(editStudentMenu.class.getName()).log(Level.SEVERE, null, ex);
                            }
                            finally{
                                if (rs != null)
                                    rs = null;
                                if (conn != null)
                                    conn = null;
                                Item item = new Item();
                                item.setVisible(true);
                                setVisible(false);
                            }
                        }
                    }
                }
                else
                    JOptionPane.showMessageDialog(null, "Select a field you would like to modify");
            }
            else{
                JOptionPane.showMessageDialog(null, "Must enter either a student ID or first name+last name");
            }
        }
    }//GEN-LAST:event_editBtnActionPerformed
   
